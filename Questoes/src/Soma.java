public class Soma {

    public static void main(String[] args) {
    
        int INDICE = 12;
        int K = 1;

        // Soma da progressão aritmética de 2 a INDICE
        int SOMA = ((INDICE * (INDICE + 1)) / 2) - ((K * (K + 1)) / 2);

        // o valor da soma é 77.
        System.out.println(SOMA);
    }
}
